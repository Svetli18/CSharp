namespace Operations
{
    public class MathOperations
    {
        public int Add(int x, int y)
        {
            return x + y;
        }

        public int Add(int x, int y,int j)
        {
            return x + y + j;
        }

        public double Add(double x, double y)
        {
            return x + y;
        }

        public double Add(double x, double y, double j) 
        {
            return x + y + j;
        }

        public decimal Add(decimal x, decimal y)
        {
            return x + y;
        }

        public decimal Add(decimal x, decimal y, decimal j)
        {
            return x + y + j;
        }
    }
}
